    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        try {
            double peso = Double.parseDouble(this.txtPeso.getText());
            double estatura = Double.parseDouble(this.txtEstatura.getText());
            
            this.doc.setPeso(peso);
            this.doc.setEstatura(estatura);
            JOptionPane.showMessageDialog(null, "Indice de masa corporal: "+this.doc.calcularIMC());

            if (this.doc.calcularIMC() < 18.5) {
                JOptionPane.showMessageDialog(null, " Su peso es inferior al normal");
            } else if ((this.doc.calcularIMC() >= 18.5) && (this.doc.calcularIMC() < 24.9)) {
                JOptionPane.showMessageDialog(null, " Su peso es normal");
            } else if ((this.doc.calcularIMC() >= 25.0) && (this.doc.calcularIMC() < 29.9)) {
                JOptionPane.showMessageDialog(null, " Su peso es superior al normal");
            } else if (this.doc.calcularIMC() > 30.0) {
                JOptionPane.showMessageDialog(null, " Obesidad");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Un error ha ocurrido", "Hey!", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnCalcularActionPerformed

